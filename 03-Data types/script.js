// 🌟***************************************🌟
//        📚 Data Types in JavaScript 📚
// 🌟***************************************🌟


// JavaScript has two main categories of data types:
// 1️⃣ Primitive Data Types
// 2️⃣ Non-Primitive Data Types


// 🌟***************************************🌟
// 1️⃣ Primitive Data Types
// 🌟***************************************🌟


// 🧱 Primitive data types represent single values. Examples:

// 🔢 Number
var number = 10; // Represents numeric values


// 📝 String
var name = "Dhruvin"; // Represents text


// ✅ Boolean
var isActive = true;  // Represents true or false values
var isComplete = false;


// ❓ Undefined
var notDefined; // A variable declared but not assigned any value


// 🛑 Null
var emptyValue = null; // Represents no value or an empty value


// 🔑 Symbol
var uniqueId = Symbol("identifier"); // A unique and immutable value


// 🌟***************************************🌟
// 2️⃣ Non-Primitive Data Types
// 🌟***************************************🌟


// 💼 Non-primitive data types can hold collections or complex entities. Examples:

// 📦 Object
var person = {
    firstName: "Dhruvin",
    lastName: "Sathvara"
}; // Represents a collection of key-value pairs


// 🛠️ Function
var greet = function () {
    console.log("👋 Hello, I am Dhruvin!");
}; // Represents a reusable block of code


// 📋 Array
var numbers = [1, 2, 3, 4]; // Represents an ordered list of values


// 🌟***************************************🌟
// 🔍 How to Find the Data Type
// 🌟***************************************🌟


// To determine the data type of a variable, use the `typeof` operator:
document.write(typeof number); // Outputs: "number"


// 🌟***************************************🌟
// 📊 Summary Table
// 🌟***************************************🌟
// | Data Type      | Example                | Description                     |
// |----------------|------------------------|---------------------------------|
// | 🔢 Number      | var x = 10;           | Numeric values                  |
// | 📝 String      | var name = "John";    | Text values                     |
// | ✅ Boolean     | var isReady = true;   | True/False values               |
// | ❓ Undefined   | var x;                | Declared but no value           |
// | 🛑 Null        | var y = null;         | Represents no value             |
// | 🔑 Symbol      | var id = Symbol();    | Unique identifier               |
// | 📦 Object      | { key: "value" }      | Collection of key-value pairs   |
// | 🛠️ Function    | function sayHi() {}   | Reusable block of code          |
// | 📋 Array       | [1, 2, 3]             | Ordered list of values          |


